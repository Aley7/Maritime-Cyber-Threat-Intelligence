# Genişletilmiş Hedef Listesi
hedef_domainler = {
    "İran Limanlar Kurumu": "pmo.ir",
    "BAE DP World (Dev Liman)": "dpworld.com",
    "Umman Limanları": "asyad.om",
    "Kuveyt Liman İdaresi": "kpa.gov.kw",
    "Suudi Arabistan Limanları": "mawani.gov.sa",
    "Katar Liman Yönetimi": "mwani.com.qa",
    "Bahreyn Limanları": "mtt.gov.bh",
    "MarineTraffic (OSINT Aracı)": "marinetraffic.com",
    "VesselFinder (OSINT Aracı)": "vesselfinder.com",
    "ABD 5. Filo (Bahreyn Üssü)": "cusnc.navy.mil"
}

osint_sonuclari = []

# Harita oluşturma (Karanlık siber tema)
siber_harita = folium.Map(location=[26.56, 56.25], zoom_start=5, tiles="CartoDB dark_matter")

for kurum, domain in hedef_domainler.items():
    try:
        # 1. IP Tespiti (DNS Recon)
        ip_adresi = socket.gethostbyname(domain)

        # 2. Coğrafi Konum Tespiti (IP Geolocation)
        geo_yanit = requests.get(f"http://ip-api.com/json/{ip_adresi}").json()

        # 3. Banner Grabbing (Sunucu Teknolojisini Çalma)
        # Sitelere bot değilmişiz gibi gidip 'Server' bilgisini çekiyoruz
        headers = {'User-Agent': 'Mozilla/5.0'}
        try:
            http_yanit = requests.get(f"https://{domain}", headers=headers, timeout=5, verify=False)
            sunucu_bilgisi = http_yanit.headers.get('Server', 'Gizlenmiş/Bulunamadı')
        except:
            sunucu_bilgisi = "Bağlantı Reddedildi (Muhtemel Güvenlik Duvarı)"

        if geo_yanit.get('status') == 'success':
            ulke = geo_yanit['country']
            sehir = geo_yanit['city']
            isp = geo_yanit['isp']
            lat = geo_yanit['lat']
            lon = geo_yanit['lon']

            # Haritaya ekleme
            popup_bilgi = f"""
            <div style='width:200px'>
            <b>🏛️ {kurum}</b><br>
            <b>🔗 Domain:</b> {domain}<br>
            <b>🌐 IP:</b> {ip_adresi}<br>
            <b>📍 Konum:</b> {ulke} / {sehir}<br>
            <b>🏢 ISP:</b> {isp}<br>
            <b>💻 Sunucu:</b> <span style='color:red;'>{sunucu_bilgisi}</span>
            </div>
            """

            folium.Marker(
                location=[lat, lon],
                popup=popup_bilgi,
                icon=folium.Icon(color='red', icon='crosshairs', prefix='fa')
            ).add_to(siber_harita)

            osint_sonuclari.append({
                "Kurum": kurum, "Domain": domain, "IP Adresi": ip_adresi,
                "Ülke": ulke, "ISP": isp, "Sunucu Teknolojisi": sunucu_bilgisi
            })

            # Terminale havalı log yazdırma
            print(f"[+] {kurum} ({domain})")
            print(f"    ↳ IP: {ip_adresi} | Konum: {ulke} ({isp})")
            print(f"    ↳ Sunucu: {sunucu_bilgisi}\n")

    except Exception as e:
        print(f"[-] {kurum} ({domain}) hedefinde hata: Hedef kendini gizliyor olabilir.\n")

print("-" * 70)
# Tabloyu ve Haritayı Kaydetme
df_altyapi = pd.DataFrame(osint_sonuclari)
df_altyapi.to_csv('maritime_cyber_infrastructure_pro.csv', index=False)
