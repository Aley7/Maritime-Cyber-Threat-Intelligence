import requests
from bs4 import BeautifulSoup
import pandas as pd
from datetime import datetime

def google_news_osint_cek():
    # Google News RSS üzerinden "Hürmüz Boğazı" araması (Türkçe kaynaklar)
    url = "https://news.google.com/rss/search?q=Hürmüz+Boğazı&hl=tr&gl=TR&ceid=TR:tr"

    headers = {
        "User-Agent": "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/91.0.4472.124 Safari/537.36"
    }

    try:
        response = requests.get(url, headers=headers)
        response.raise_for_status()

        # RSS bir XML formatı olduğu için 'xml' modunda okuyoruz
        soup = BeautifulSoup(response.content, 'xml')

        # RSS içindeki her bir haber 'item' etiketi içinde tutulur
        haber_kartlari = soup.find_all('item')

        haber_listesi = []

        # Çektiğimiz haberleri tek tek dönüp ayıklıyoruz
        for kart in haber_kartlari:
            baslik = kart.find('title').text if kart.find('title') else 'Başlık Yok'
            link = kart.find('link').text if kart.find('link') else 'Link Yok'
            yayin_tarihi = kart.find('pubDate').text if kart.find('pubDate') else 'Tarih Yok'

            haber_listesi.append({
                'Cekilme_Zamani': datetime.now().strftime("%Y-%m-%d %H:%M"),
                'Yayin_Tarihi': yayin_tarihi,
                'Haber_Basligi': baslik,
                'Haber_Linki': link
            })

        return haber_listesi

    except Exception as e:
        print(f"Veri çekilirken kritik bir hata oluştu: {e}")
        return []

